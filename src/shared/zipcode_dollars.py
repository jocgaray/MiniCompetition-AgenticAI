import pandas as pd

try:
    from uszipcode import SearchEngine
    search = SearchEngine()
except Exception:
    search = None


def transform_coordinates_to_price(df_input: pd.DataFrame) -> pd.DataFrame:
    df = df_input.copy()

    df["region_price"] = df.apply(
        lambda row: get_price_from_lat_long(
            float(row.get("latitude", 0)), float(row.get("longitude", 0))
        ),
        axis=1,
    )

    df = df.drop(columns=["latitude", "longitude"])

    return df


def get_price_from_lat_long(lat: float, lon: float):
    if search is not None:
        try:
            result = search.by_coordinates(lat=lat, lng=lon, returns=1)
            if result:
                return zip_price_data.get(result[0].zipcode, 500000)
        except Exception:
            pass
    return 500000


